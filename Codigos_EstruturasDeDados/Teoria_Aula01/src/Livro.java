
public class Livro {
	private String nome;
	private String autor;
	
	public Livro(String n, String a) {
		super();
		this.nome = n;
		this.autor = a;
	}
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getAutor() {
		return autor;
	}
	public void setAutor(String autor) {
		this.autor = autor;
	}
	

}
